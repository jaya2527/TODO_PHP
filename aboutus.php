<p align="center">The best to-do list apps:

<h4 align="center">Make it fast to add and organize
    tasks.</h4> <p align="center">Ideally, a task is added and categorized in a couple taps or keystrokes. <br>If adding your to-dos is a hassle, you just won't do it.

<h4 align="center">Offer multiple ways to organize your tasks.</h4> <p align="center">Tags, lists, projects, and due dates are all helpful, and the
